    private void JB_NewUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JB_NewUserActionPerformed
        // TODO add your handling code here:
        /*
        Creacion del objeto persona "Usuario" 
        */
        if (JT_NewNombre.getText().length() == 0) {
            //checa si no hay nada escrito en el jText de usuario
            JOptionPane.showMessageDialog(null, "Debes ingresar un nombre");
            JT_NewNombre.requestFocus();
            return;
        }
        
        if (JT_NewDireccion.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Debes ingresar una direccion");
            //checa si no hay nada escrito en el jText de contrase単a
            JT_NewDireccion.requestFocus();
            return;
        }
        
        if (JT_NewCURP.getText().length() == 0){
            JOptionPane.showMessageDialog(null, "Debes ingresar una CURP");
            JT_NewCURP.requestFocus();
            return;
        }
        
        try{
            //Nombre, Direccion, CURP, Dia, Mes, A単o
            Usuario User = new Usuario( JT_NewNombre.getText(), JT_NewDireccion.getText(), JT_NewCURP.getText(),
                    JL_NewDia.getSelectedIndex(), JL_NewMes.getSelectedIndex(), JL_NewA単o.getSelectedIndex()
            );//termina constructor de Usuario
            
            //calculo rapido de la edad, segun la fecha establecida por el usuario
        }catch(Exception e){
            
        }
        finally{
            JOptionPane.showMessageDialog(this, "Usuario creado");
            this.dispose();
            Jframe_menu menu = new Jframe_menu();
            menu.setVisible(true);
        }
    }//GEN-LAST:event_JB_NewUserActionPerformed

